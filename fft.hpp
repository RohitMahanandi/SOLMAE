vector<vector<Complex>> split_fft(vector<Complex> f_fft)
{
     int n = f_fft.size();
    std::vector<Complex> w = roots_dict[n];
    std::vector<Complex> f0_fft(n / 2);
    std::vector<Complex> f1_fft(n / 2);

    for (int i = 0; i < n / 2; ++i) {
        f0_fft[i] = 0.5 * (f_fft[2 * i] + f_fft[2 * i + 1]);
        f1_fft[i] = 0.5 * (f_fft[2 * i] - f_fft[2 * i + 1]) * std::conj(w[2 * i]);
    }

    std::vector<std::vector<Complex>> result;
    result.push_back(f0_fft);
    result.push_back(f1_fft);

    return result;
    
}



vector<Complex> merge_fft(const vector<Complex>& f0_fft, const vector<Complex>& f1_fft) {
    int n = 2 * f0_fft.size();
    vector<Complex> w = roots_dict[n];
    vector<Complex> f_fft(n);

    for (int i = 0; i < n / 2; ++i) {
        f_fft[2 * i + 0] = f0_fft[i] + w[2 * i] * f1_fft[i];
        f_fft[2 * i + 1] = f0_fft[i] - w[2 * i] * f1_fft[i];
    }

    return f_fft;
}


vector<Complex> fft(const vector<Complex>& f) {
    int n = f.size();
    if (n > 2) {
        // Split the input polynomial f
        vector<Complex> f0(n / 2);
        vector<Complex> f1(n / 2);
        for (int i = 0; i < n / 2; ++i) {
            f0[i] = 0.5 * (f[2 * i] + f[2 * i + 1]);
            f1[i] = 0.5 * (f[2 * i] - f[2 * i + 1]);
        }

        // Compute the FFT of f0 and f1
        vector<Complex> f0_fft = fft(f0);
        vector<Complex> f1_fft = fft(f1);

        // Merge the results using merge_fft
        return merge_fft(f0_fft, f1_fft);
    } else if (n == 2) {
        // Base case for n == 2
        vector<Complex> f_fft(2);
        f_fft[0] = f[0] + complex<double>(0.0, 1.0) * f[1];
        f_fft[1] = f[0] - complex<double>(0.0, 1.0) * f[1];
        return f_fft;
    }

    // Return an empty vector if n <= 2 (or any other appropriate handling)
    return vector<Complex>();
}

