typedef complex<double> Complex;
 
vector<Complex> merge(const vector<Complex>& f0, const vector<Complex>& f1) {
    int n = f0.size();
    vector<Complex> f(n);

    for (int i = 0; i < n; ++i) {
        f[i] = f0[i] + f1[i];
    }

    return f;
}



