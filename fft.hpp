vector<Complex> sub_fft(const vector<Complex>& f_fft, const vector<Complex>& g_fft) {
    assert(f_fft.size() == g_fft.size());
    int deg = f_fft.size();
    vector<Complex> result(deg);

    for (int i = 0; i < deg; i++) {
        result[i] = f_fft[i] - g_fft[i];
    }

    return result;
}



vector<Complex> div_fft(const vector<Complex>& f_fft, const vector<Complex>& g_fft) {
    assert(f_fft.size() == g_fft.size());
    int deg = f_fft.size();
    vector<Complex> result(deg);

    for (int i = 0; i < deg; i++) {
        result[i] = f_fft[i] / g_fft[i];
    }

    return result;
}

vector<Complex> adj_fft(const vector<Complex>& f_fft) {
    int deg = f_fft.size();
    vector<Complex> result(deg);

    for (int i = 0; i < deg; i++) {
        result[i] = conj(f_fft[i]);
    }

    return result;
}

vector<Complex> div(const vector<Complex>& f, const vector<Complex>& g) {
    return ifft(div_fft(fft(f), fft(g)));
}

vector<Complex> adj(const vector<Complex>& f) {
    return ifft(adj_fft(fft(f)));
}

