vector<Complex> add(const vector<Complex>& f, const vector<Complex>& g) {
    assert(f.size() == g.size());
    int deg = f.size();
    vector<Complex> result(deg);

    for (int i = 0; i < deg; i++) {
        result[i] = f[i] + g[i];
    }

    return result;
}

vector<Complex> neg(const vector<Complex>& f) {
    int deg = f.size();
    vector<Complex> result(deg);

    for (int i = 0; i < deg; i++) {
        result[i] = -f[i];
    }

    return result;
}

vector<Complex> sub(const vector<Complex>& f, const vector<Complex>& g) {
    assert(f.size() == g.size());
    return add(f, neg(g));
}

vector<Complex> mul_fft(const vector<Complex>& f_fft, const vector<Complex>& g_fft) {
    assert(f_fft.size() == g_fft.size());
    int deg = f_fft.size();
    vector<Complex> result(deg);

    for (int i = 0; i < deg; i++) {
        result[i] = f_fft[i] * g_fft[i];
    }

    return result;
}

vector<Complex> mul(const vector<Complex>& f, const vector<Complex>& g) {
    return ifft(mul_fft(fft(f), fft(g)));
}



